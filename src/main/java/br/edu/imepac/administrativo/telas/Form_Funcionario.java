    private void jB_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_excluirActionPerformed
       int opcao = jTabela_funcionario.getSelectedRow();
       if(opcao >= 0){
           Funcionario f = new Funcionario();
           f.setId_funcionario(Integer.parseInt(jTabela_funcionario.getValueAt(opcao, 0).toString()));
           f.setNome(jTabela_funcionario.getValueAt(opcao, 1).toString());
           f.setCpf(jTabela_funcionario.getValueAt(opcao, 2).toString());
           f.setEmail(jTabela_funcionario.getValueAt(opcao, 3).toString());
           f.setTelefone(jTabela_funcionario.getValueAt(opcao, 4).toString());
           f.setDataadmissao(jTabela_funcionario.getValueAt(opcao, 5).toString());
           
           FuncionarioDAO fdao = new FuncionarioDAO();
           fdao.excluir(f);
           preencherTabela();
       }else{
           JOptionPane.showMessageDialog(null, "Selecione uma linha!");
       }
        
    }//GEN-LAST:event_jB_excluirActionPerformed

