    private void jB_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_atualizarActionPerformed
 Funcionario f = new Funcionario();
     f.setId_funcionario(Integer.parseInt(jT_id.getText()));
        f.setNome(jT_nome.getText());
        f.setCpf(jF_cpf.getText());
        f.setEmail(jT_email.getText());
        f.setTelefone(jF_admissao.getText());
        f.setDataadmissao(jF_admissao.getText());
        f.setSenha(jP_senha.getText());
        
        FuncionarioDAO fdao= new FuncionarioDAO();
        fdao.cadastrar(f);
        preencherTabela();
        jTabbedPane1.setSelectedIndex(1);
        jB_atualizar.setVisible(false);
        jB_salvar.setVisible(true);
        jP_senha.setEditable(true);
    }//GEN-LAST:event_jB_atualizarActionPerformed

    private void jB_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_editarActionPerformed
int opcao = jTabela_funcionario.getSelectedRow();
       if(opcao >= 0){
          
           jT_id.setText(jTabela_funcionario.getValueAt(opcao, 0).toString());
           jT_nome.setText(jTabela_funcionario.getValueAt(opcao, 1).toString());
           jF_cpf.setText(jTabela_funcionario.getValueAt(opcao, 2).toString());
           jT_email.setText(jTabela_funcionario.getValueAt(opcao, 3).toString());
           jF_telefone.setText(jTabela_funcionario.getValueAt(opcao, 4).toString());
           jF_admissao.setText(jTabela_funcionario.getValueAt(opcao, 5).toString());          
           jTabbedPane1.setSelectedIndex(0);
           jB_atualizar.setVisible(true);
           jB_salvar.setVisible(false);
           jP_senha.setEditable(false);
       }else{
           JOptionPane.showMessageDialog(null, "Selecione uma linha!");
       }    }//GEN-LAST:event_jB_editarActionPerformed

